    // Distance-based stand-in for attention coefficients, computed the way a
    // GAT layer would score neighbors: softmax over negative distance, so
    // attention is peaked on the closest neighbor(s) rather than spread evenly.
    function attentionFor(id) {
      var nbrs = neighbors[id] || [];
      var p = nodeXYZ[id];
      var weights = {};
      if (!nbrs.length || !p) return weights;
      var dists = nbrs.map(function (nid) {
        var q = nodeXYZ[nid];
        return Math.hypot(q.x - p.x, q.y - p.y, q.z - p.z);
      });
      var minD = Math.min.apply(null, dists);
      var temp = Math.max(minD * 0.5, 0.05);
      var scores = dists.map(function (d) { return Math.exp(-d / temp); });
      var sum = scores.reduce(function (a, b) { return a + b; }, 0);
      var soft = scores.map(function (s) { return s / sum; });
      var maxS = Math.max.apply(null, soft);
      var minS = Math.min.apply(null, soft);
      nbrs.forEach(function (nid, i) {
        var t = maxS === minS ? 1 : (soft[i] - minS) / (maxS - minS);
        weights[nid] = 0.16 + 0.84 * t;
      });
      return weights;
    }

